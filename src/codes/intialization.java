package codes;

public class intialization {
	
	public static void main(String[] args)
	{
	
//		INTEGERY ARRAY
		int roll[] = {11,22,33,44,55,66,77};
		System.out.println("Integer array");
		
		for( int i=0;i<roll.length;i++)
		{
			System.out.print(roll[i]+" ");
		}
		
		
		System.out.println("\n\n");
		
//		STRING ARRAY
		String coffie[] = {"1.Hot Coffie","2.Cold Coffie", "3.Chocoloate Coffie","4.Americano Coffie"};
		System.out.println("String Array : ");
		
		for(int i=0; i<coffie.length; i++)
		{
			System.out.print(coffie[i]+" ");
		}
		
		
	}

}
